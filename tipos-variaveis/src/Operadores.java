public class Operadores {

    public static void main(String[] args) {

        String nomeCompleto = "nome1" + "nome2";
        System.out.println(nomeCompleto);

        int a, b;
        a = 5;
        b = 6;

        // ternario
        String resultado = a == b ? "verdade" : "falso";

        System.out.println(resultado);

    }

}
